const variantCharacter = /\uFE0F/g;
const zeroWidthJoiner = "\u200D";

/**
 * @param {string} emoji
 */
function getUnicodeCodePoint(emoji) {
  const normalized = emoji.indexOf(zeroWidthJoiner) < 0 ? emoji.replace(variantCharacter, "") : emoji;

  const result = [];
  for (const codePoint of normalized) {
    const point = codePoint.codePointAt(0);
    if (point !== undefined) {
      result.push(point.toString(16));
    }
  }
  return result.join("-");
}

/**
 * Remove the possible variant and convert utf16 into code points.
 * If there is a zero-width-joiner (U+200D), leave the variants in.
 *
 * @param {string} emoji - actual character, eg рҹҳӮ
 */
function getEmojiImage(emoji) {
  const codePoint = getUnicodeCodePoint(emoji);
  // or png: https://cdn.jsdelivr.net/gh/jdecked/twemoji@15.0.2/assets/72x72/1f004.png
  return `https://cdn.jsdelivr.net/gh/jdecked/twemoji@15.0.2/assets/svg/${codePoint}.svg`;
}

